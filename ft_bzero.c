#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*dst;

	i = 0;
	dst = s;
	while (n > 0 && dst[i])
	{
		dst[i] = '\0';
		i++;
		n--;
	}
}
